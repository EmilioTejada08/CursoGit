import math

"""
Estas son funciones matemáticas.
"""


def sumar(n1, n2):
    print(f"La suma es {n1+n2}")


def restar(n1, n2):
    print(f"La resta es {n1-n2}")


def multiplicar(n1, n2):
    print(f"La multiplicación es {n1*n2}")


def dividir(n1, n2):
    print(f"La división es {n1/n2}")


def potencia(n1, n2):
    print(f"La potencia es {n1**n2}")


def raiz(n1, n2):
    print(f"La raíz cuadrada de {n1} es {math.sqrt(n1)}")
    print(f"La raíz cuadrada de {n2} es {math.sqrt(n2)}")


def modulo(n1, n2):
    print(f"El módulo es {n1 % n2}")


def division_entera(n1, n2):
    print(f"La división entera es {n1//n2}")
